/**
* Определяет контракт для вычисления числовых характеристик.
* @summary Базовый интерфейс для различных вычислений над числами.
*/
interface NumberCharacteristic {
  /**
   * Вычисляет характеристику числа
   * @param number число для анализа
   * @return результат вычисления характеристики
   */
  int count(int number);
}
